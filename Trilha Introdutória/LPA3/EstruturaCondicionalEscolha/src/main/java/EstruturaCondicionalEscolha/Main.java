package EstruturaCondicionalEscolha;

public class Main {

    public static void main(String[] args) {
        
        int codigoProduto = 2;
        
        switch(codigoProduto){
            case 1:
                System.out.println("1");  
                break;
                
            case 2:
                System.out.println("2");  
                break;
                
               default:
                   
                   break;
        }
    }
    
}
